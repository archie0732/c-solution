def fun(current, n, k):
    n = max(current, n)
    k = min(current, k)

    sumLtoR = (current * (current + 1)) // 2 - (k * (k - 1)) // 2

    sumRtoL = abs((n * (n + 1)) // 2 - (current * (current + 1)) // 2)

    result = abs(sumLtoR - sumRtoL)

    return result


def solve():
    # n 是陣列的長度 而 k 是陣列的起點位置
    n, k = map(int, input().split())
    n = n + k - 1
    current = (n + k) // 2
    left = k
    right = n
    ans = 0

    while True:
        nxt = fun(current + 1, n, k)
        pre = fun(current - 1, n, k)
        curr = fun(current, n, k)

        if curr <= pre and curr <= nxt:
            ans = curr
            break

        if curr < nxt:
            right = current
        elif curr < pre:
            left = current

        current = (left + right) // 2

    print(ans)


t = int(input())
for _ in range(t):
    solve()
